        /// <summary>
        /// Runs <paramref name="func"/> on a fresh STA thread and returns its result.
        /// Both construction AND assertions must happen inside the same STA call so
        /// WPF never raises "a different thread owns this object".
        /// </summary>
        private static T RunSta<T>(Func<T> func)
        {
            T result = default;
            Exception ex = null;

            var thread = new Thread(() =>
            {
                try { result = func(); }
                catch (Exception e) { ex = e; }
            });

            thread.SetApartmentState(ApartmentState.STA);
            thread.Start();
            thread.Join();

            if (ex != null) System.Runtime.ExceptionServices.ExceptionDispatchInfo.Capture(ex).Throw();
            return result;
        }

        /// <summary>Runs an assertion-only action on an STA thread.</summary>
        private static void RunSta(Action action) => RunSta<int>(() => { action(); return 0; });

        private static string FlattenInlines(InlineCollection inlines)
            => string.Concat(inlines.OfType<Run>().Select(r => r.Text));

        private static bool HasNestedHyperlink(InlineCollection inlines)
        {
            foreach (var inline in inlines)
            {
                if (inline is Hyperlink) return true;
                if (inline is Span span && HasNestedHyperlink(span.Inlines))
                    return true;
            }
            return false;
        }

