                Path.GetTempPath(),
                "SkymuTests_Version_" + Guid.NewGuid().ToString("N"));
            Directory.CreateDirectory(_folder);
        }

        public void Dispose()
        {
            try { Directory.Delete(_folder, recursive: true); } catch { }
        }

        [Fact]
        public void Constructor_CreatesValidXmlConfigFile()
        {
            new DatabaseManager(user: null, custom_db_folder: _folder);

            string configPath = Path.Combine(_folder, "config.xml");
            Assert.True(File.Exists(configPath));

            var doc = System.Xml.Linq.XDocument.Load(configPath);
            Assert.NotNull(doc.Root);
            var versionEl = doc.Root.Element("DatabaseVersion");
            Assert.NotNull(versionEl);
            Assert.True(int.TryParse(versionEl.Value, out int v) && v >= 1);
        }

        [Fact]
        public void Constructor_CorruptConfigFile_WipesAndRecreates()
        {
            // Write a corrupt config — constructor should wipe and rebuild rather than crash.
            string configPath = Path.Combine(_folder, "config.xml");
            File.WriteAllText(configPath, "THIS IS NOT XML <<<");

            var ex = Record.Exception(
                () => new DatabaseManager(user: null, custom_db_folder: _folder));
            Assert.Null(ex);
            Assert.True(File.Exists(configPath));

            // Should now be valid XML
            var doc = System.Xml.Linq.XDocument.Load(configPath);
            Assert.NotNull(doc.Root?.Element("DatabaseVersion"));
        }

        [Fact]
        public void Constructor_StaleVersionInConfig_WipesDb()
        {
            // First, create a DB with some data.
            var db = new DatabaseManager(user: null, custom_db_folder: _folder);
            var dm = Make.DM("dm-a", "a");
            db.Conversations.Write(new[] { dm });

            // Now write a config claiming version 0 (older than current).
            string configPath = Path.Combine(_folder, "config.xml");
            new System.Xml.Linq.XDocument(
                new System.Xml.Linq.XElement("SkymuDatabase",
                    new System.Xml.Linq.XElement("DatabaseVersion", 0))
            ).Save(configPath);

            // Recreating the manager should wipe the old database.
            var db2 = new DatabaseManager(user: null, custom_db_folder: _folder);
            Assert.Empty(db2.Conversations.Read());
        }
