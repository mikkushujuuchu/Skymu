        /// <summary>
        /// Starts a bare TCP listener on an OS-assigned port, accepts exactly
        /// one connection, runs <paramref name="handler"/> on it, then stops.
        /// Returns the port number so the caller can connect.
        /// </summary>
        public static async Task<int> RunOnceAsync(
            Func<Stream, CancellationToken, Task> handler,
            CancellationToken ct = default)
        {
            var listener = new TcpListener(IPAddress.Loopback, 0);
            listener.Start();
            int port = ((IPEndPoint)listener.LocalEndpoint).Port;

            _ = Task.Run(async () =>
            {
                try
                {
                    using var client = await listener.AcceptTcpClientAsync().ConfigureAwait(false);
                    await handler(client.GetStream(), ct).ConfigureAwait(false);
                }
                catch { /* test server; swallow */ }
                finally { listener.Stop(); }
            }, ct);

            return port;
        }

        public static Task<int> ServeHttp(
            int statusCode,
            string body,
            string extraHeaders = "",
            CancellationToken ct = default)
        {
            return RunOnceAsync(async (stream, token) =>
            {
                byte[] bodyBytes = Encoding.UTF8.GetBytes(body);
                string response =
                    $"HTTP/1.1 {statusCode} OK\r\n" +
                    $"Content-Length: {bodyBytes.Length}\r\n" +
                    $"Content-Type: text/plain\r\n" +
                    extraHeaders +
                    "Connection: close\r\n" +
                    "\r\n";

                byte[] head = Encoding.ASCII.GetBytes(response);
                await stream.WriteAsync(head, 0, head.Length, token).ConfigureAwait(false);
                await stream.WriteAsync(bodyBytes, 0, bodyBytes.Length, token).ConfigureAwait(false);
                await stream.FlushAsync(token).ConfigureAwait(false);
            }, ct);
        }

        public static Task<int> ServeWebSocket(
            string expectedSubProtocol = null,
            Func<Stream, CancellationToken, Task> afterUpgrade = null,
            CancellationToken ct = default)
        {
            return RunOnceAsync(async (stream, token) =>
            {
                // Read upgrade request
                var sb = new StringBuilder();
                var buf = new byte[4096];
                string rawRequest = "";
                while (true)
                {
                    int n = await stream.ReadAsync(buf, 0, buf.Length, token).ConfigureAwait(false);
                    rawRequest += Encoding.ASCII.GetString(buf, 0, n);
                    if (rawRequest.Contains("\r\n\r\n")) break;
                }

                // Extract Sec-WebSocket-Key
                string key = "";
                foreach (var line in rawRequest.Split(new[] { "\r\n" }, StringSplitOptions.None))
                {
                    if (line.StartsWith("Sec-WebSocket-Key:", StringComparison.OrdinalIgnoreCase))
                    {
                        key = line.Substring("Sec-WebSocket-Key:".Length).Trim();
                        break;
                    }
                }

                string accept = ComputeAccept(key);
                string upgradeResponse =
                    "HTTP/1.1 101 Switching Protocols\r\n" +
                    "Upgrade: websocket\r\n" +
                    "Connection: Upgrade\r\n" +
                    $"Sec-WebSocket-Accept: {accept}\r\n";

                if (!string.IsNullOrEmpty(expectedSubProtocol))
                    upgradeResponse += $"Sec-WebSocket-Protocol: {expectedSubProtocol}\r\n";

                upgradeResponse += "\r\n";

                byte[] upBytes = Encoding.ASCII.GetBytes(upgradeResponse);
                await stream.WriteAsync(upBytes, 0, upBytes.Length, token).ConfigureAwait(false);
                await stream.FlushAsync(token).ConfigureAwait(false);

                if (afterUpgrade != null)
                    await afterUpgrade(stream, token).ConfigureAwait(false);
            }, ct);
        }

        private static string ComputeAccept(string key)
        {
            const string Magic = "258EAFA5-E914-47DA-95CA-C5AB0DC85B11";
            using var sha1 = SHA1.Create();
            byte[] combined = Encoding.ASCII.GetBytes(key + Magic);
            return Convert.ToBase64String(sha1.ComputeHash(combined));
        }

        /// <summary>Builds an unmasked server→client WebSocket text frame.</summary>
        public static byte[] BuildServerFrame(string text)
        {
            byte[] payload = Encoding.UTF8.GetBytes(text);
            var frame = new List<byte>();
            frame.Add(0x81); // FIN + text opcode
            if (payload.Length < 126)
                frame.Add((byte)payload.Length);
            else
            {
                frame.Add(126);
                frame.Add((byte)(payload.Length >> 8));
                frame.Add((byte)(payload.Length & 0xFF));
            }
            frame.AddRange(payload);
            return frame.ToArray();
        }

        /// <summary>Builds an unmasked server→client close frame (1000).</summary>
        public static byte[] BuildCloseFrame(ushort code = 1000)
        {
            return new byte[] { 0x88, 0x02, (byte)(code >> 8), (byte)(code & 0xFF) };
        }
    }

