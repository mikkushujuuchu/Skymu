    /// <summary>
    /// These tests exercise the wildcard / exact-match hostname logic
    /// that lives inside BouncyCertAuth.NameMatches (private static).
    /// Because it is private, we test it indirectly through observable
    /// behaviour: a connection whose server sends a cert with a
    /// non-matching hostname must be rejected.  However since we cannot
    /// spin up a real TLS server in a unit test, we validate the
    /// NameMatches logic via a thin public wrapper exposed for testing.
    /// If your project has a test-seam / InternalsVisibleTo you can
    /// call NameMatches directly; otherwise the helper below replicates
    /// the same logic so the rules are verified independently.
    /// </summary>
    public class NameMatchTests
    {
        // Replicated from BifrostTLS.BouncyCertAuth.NameMatches
        private static bool NameMatches(string pattern, string host)
        {
            if (string.IsNullOrEmpty(pattern) || string.IsNullOrEmpty(host))
                return false;

            if (string.Equals(pattern, host, StringComparison.OrdinalIgnoreCase))
                return true;

            if (!pattern.StartsWith("*.") || pattern.Length < 3)
                return false;

            string suffix = pattern.Substring(1); // ".example.com"

            int dotCount = 0;
            foreach (char c in suffix) if (c == '.') dotCount++;
            if (dotCount < 2) return false;

            if (!host.EndsWith(suffix, StringComparison.OrdinalIgnoreCase))
                return false;

            string leftLabel = host.Substring(0, host.Length - suffix.Length);
            return leftLabel.Length > 0 && leftLabel.IndexOf('.') < 0;
        }

        [Theory]
        [InlineData("skymu.app", "skymu.app", true)]
        [InlineData("skymu.app", "SKYMU.APP", true)]  // case-insensitive
        [InlineData("skymu.app", "evil.com", false)]
        [InlineData("*.skymu.app", "api.skymu.app", true)]
        [InlineData("*.skymu.app", "skymu.app", false)] // wildcard needs left label
        [InlineData("*.skymu.app", "a.b.skymu.app", false)] // only one label allowed
        [InlineData("*.co.uk", "skymu.co.uk", false)] // suffix has only 2 dots → rejected
        [InlineData("", "skymu.app", false)]
        [InlineData("skymu.app", "", false)]
        [InlineData("*.skymu.app", "API.SKYMU.APP", true)]  // wildcard + case-insensitive
        [InlineData("notawildcard.app", "sub.notawildcard.app", false)]
        public void NameMatches_Theory(string pattern, string host, bool expected)
        {
            Assert.Equal(expected, NameMatches(pattern, host));
        }
    }
