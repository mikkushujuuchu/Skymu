        private static async Task ReadExact(Stream stream, byte[] buf, CancellationToken ct)
        {
            int read = 0;
            while (read < buf.Length)
            {
                int n = await stream.ReadAsync(buf, read, buf.Length - read, ct)
                    .ConfigureAwait(false);
                if (n == 0) throw new EndOfStreamException();
                read += n;
            }
        }

        private static string ComputeAccept(string key)
        {
            const string Magic = "258EAFA5-E914-47DA-95CA-C5AB0DC85B11";
            using var sha1 = SHA1.Create();
            byte[] combined = Encoding.ASCII.GetBytes(key + Magic);
            return Convert.ToBase64String(sha1.ComputeHash(combined));
        }
