using BenchmarkDotNet.Attributes;
using BenchmarkDotNet.Configs;
using BenchmarkDotNet.Jobs;
using BenchmarkDotNet.Running;
using BenchmarkDotNet.Toolchains.InProcess.Emit;
using Skymu.Formatting;
using System.Threading;

namespace Skymu.Benchmarks
{
    // InProcessEmitToolchain runs every benchmark iteration inside THIS process
    // rather than spawning a child process. That means they inherit the STA
    // apartment state set in Program.Main, which WPF requires for TextBlock etc.
    //
    // Trade-off: slightly less process isolation (e.g. a static field mutated by
    // one benchmark can affect another), but that is not a concern for Formatter
    // since it is purely functional / allocation-only.
    [MemoryDiagnoser]
    [Config(typeof(InProcessConfig))]
    public class FormatterBenchmarks
    {
        private class InProcessConfig : ManualConfig
        {
            public InProcessConfig()
            {
                AddJob(Job.Default.WithToolchain(InProcessEmitToolchain.Instance));
            }
        }

        // -------------------------------------------------------
        // Inputs representing realistic message content
        // -------------------------------------------------------

        private const string PlainText =
            "Hello, this is a plain message with no formatting at all.";

        private const string BoldItalic =
            "This has **bold** and *italic* and ***both*** and ~~strikethrough~~ text.";

        private const string WithUrl =
            "Check out https://skymu.app and also https://github.com for more info.";

        private const string WithEmoji =
            "Hello 😀 how are you 👍 doing today 🎉 this is great 🔥";

        private const string WithCode =
            "Use `Console.WriteLine()` or a block:\n```csharp\nvar x = 1;\nConsole.WriteLine(x);\n```";

        private const string WithTable =
            "| Name | Value |\n|------|-------|\n| Foo | 123 |\n| Bar | 456 |";

        private const string Combined =
            "**Hey** 😀 check https://skymu.app and use `code` and ~~old~~ *stuff* too!";

        private const string LongPlain =
            "This is a longer plain text message that simulates something a user might actually " +
            "type in a chat window. It has no special formatting, just words and sentences. " +
            "The quick brown fox jumps over the lazy dog. Pack my box with five dozen liquor jugs.";

        private const string LongMarkdown =
            "# Heading\n\nThis is **bold** and *italic* with a [link](https://skymu.app).\n\n" +
            "- Item one with `code`\n- Item two with ~~strike~~\n- Item three\n\n" +
            "```\nsome code block here\n```\n\n> A blockquote with *emphasis*";

        private const string LangSimple =
            "Hello %s, you have %d messages waiting.";

        private const string LangHtml =
            "Click <a href=\"https://skymu.app\">here</a> for <b>help</b> or <br/>contact us.";

        // -------------------------------------------------------
        // Benchmarks
        // -------------------------------------------------------

        [Benchmark(Baseline = true, Description = "Plain text, no formatting")]
        public object Baseline()
            => Formatter.Parse(PlainText, doNotFormat: true);

        [Benchmark(Description = "Plain text through full pipeline")]
        public object PlainFormatted()
            => Formatter.Parse(PlainText);

        [Benchmark(Description = "Bold, italic, strikethrough")]
        public object BoldItalic_Formatted()
            => Formatter.Parse(BoldItalic);

        [Benchmark(Description = "URLs only")]
        public object Url_Formatted()
            => Formatter.Parse(WithUrl);

        [Benchmark(Description = "Emoji only")]
        public object Emoji_Formatted()
            => Formatter.Parse(WithEmoji);

        [Benchmark(Description = "Code inline and block")]
        public object Code_Formatted()
            => Formatter.Parse(WithCode);

        [Benchmark(Description = "Pipe table")]
        public object Table_Formatted()
            => Formatter.Parse(WithTable);

        [Benchmark(Description = "Combined markdown + emoji + URL")]
        public object Combined_Formatted()
            => Formatter.Parse(Combined);

        [Benchmark(Description = "Long plain text")]
        public object LongPlain_Formatted()
            => Formatter.Parse(LongPlain);

        [Benchmark(Description = "Long markdown document")]
        public object LongMarkdown_Formatted()
            => Formatter.Parse(LongMarkdown);

        [Benchmark(Description = "Lang text with %s substitution")]
        public object Lang_Simple()
            => Formatter.ProcessLangText(LangSimple, new object[] { "OmegaAOL", 5 });

        [Benchmark(Description = "Lang text with HTML tags")]
        public object Lang_Html()
            => Formatter.ProcessLangText(LangHtml, new object[0]);
    }
}